use piet::Text as _;
use piet_cosmic_text::Text;

const TUFFY: &[u8] = include_bytes!("../fonts/Tuffy.ttf");

#[test]
fn load_font() {
    let mut text = Text::new();
    let name = text.load_font(TUFFY).unwrap();
    assert_eq!(name.name(), "Tuffy");
}
